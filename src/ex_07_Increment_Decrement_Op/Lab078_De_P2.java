package ex_07_Increment_Decrement_Op;

public class Lab078_De_P2 {
    public static void main(String[] args) {

        int a = 10;
        int result = --a;
        System.out.println(result); // first decrement so ans 9
        System.out.println(a); // value was already assigned so decrement value will be assigned to it ans 9
    }
}
